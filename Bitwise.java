public class Bitwise {
    public static void main(String[] args) {
        int x = 5, y = 6;
        System.out.println("x= "+x);
        System.out.println("y= "+y);
        System.out.println("x & y = " + (x & y));
        System.out.println("x | y = " + (x | y));
        System.out.println("x ^ y = " + (x ^ y));
    }
}

//Kesimpulan : Penggunaan operator bitwise, dimana tipe data akan dioperasikan seperti logika proporsi setelah data dirubah menjadi biner terlebih dahulu